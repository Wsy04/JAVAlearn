package JavaObject2;

public class Sportsman {
    private String name;
    private int age;

    public Sportsman() {
    }

    public Sportsman(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
